# =====================================================================
cells.append(md([
    "---\n",
    "\n",
    "## Step 8: Hybrid Loss Function (Novelty 3)\n",
    "\n",
    "```\n",
    "L = alpha * L_RMSE + beta * L_trend + gamma * L_volatility + delta * L_spatial\n",
    "```\n",
    "\n",
    "| Component | Weight | Purpose |\n",
    "|-----------|--------|---------|\n",
    "| L_RMSE | alpha=0.4 | Point-wise accuracy |\n",
    "| L_trend | beta=0.2 | Direction consistency |\n",
    "| L_volatility | gamma=0.2 | Variance matching |\n",
    "| L_spatial | delta=0.2 | Spatial coherence |"
]))

cells.append(code([
    "class HybridLoss(nn.Module):\n",
    "    \"\"\"Hybrid Loss = alpha*RMSE + beta*Trend + gamma*Volatility + delta*Spatial\"\"\"\n",
    "\n",
    "    def __init__(self, alpha=0.4, beta=0.2, gamma=0.2, delta=0.2, adj_matrix=None):\n",
    "        super().__init__()\n",
    "        self.alpha, self.beta, self.gamma, self.delta = alpha, beta, gamma, delta\n",
    "        if adj_matrix is not None:\n",
    "            self.register_buffer('adj', torch.tensor(adj_matrix, dtype=torch.float32))\n",
    "        else:\n",
    "            self.adj = None\n",
    "\n",
    "    def forward(self, y_pred, y_true):\n",
    "        # 1. RMSE\n",
    "        mse = torch.mean((y_true - y_pred) ** 2)\n",
    "        rmse_loss = torch.sqrt(mse + 1e-8)\n",
    "\n",
    "        # 2. Trend Error\n",
    "        if y_pred.shape[0] > 1:\n",
    "            true_diff = y_true[1:] - y_true[:-1]\n",
    "            pred_diff = y_pred[1:] - y_pred[:-1]\n",
    "            sign_match = torch.sign(true_diff) * torch.sign(pred_diff)\n",
    "            trend_loss = torch.mean(torch.clamp(1.0 - sign_match, min=0.0))\n",
    "        else:\n",
    "            trend_loss = torch.tensor(0.0, device=y_pred.device)\n",
    "\n",
    "        # 3. Volatility Error\n",
    "        volatility_loss = torch.abs(torch.var(y_true) - torch.var(y_pred))\n",
    "\n",
    "        # 4. Spatial Error\n",
    "        if self.adj is not None:\n",
    "            residuals = y_true - y_pred\n",
    "            r_diff = residuals.unsqueeze(2) - residuals.unsqueeze(1)\n",
    "            spatial_loss = torch.mean((r_diff ** 2) * self.adj.unsqueeze(0))\n",
    "        else:\n",
    "            spatial_loss = torch.tensor(0.0, device=y_pred.device)\n",
    "\n",
    "        total = (self.alpha * rmse_loss + self.beta * trend_loss\n",
    "                 + self.gamma * volatility_loss + self.delta * spatial_loss)\n",
    "\n",
    "        components = {\n",
    "            'rmse': rmse_loss.item(), 'trend': trend_loss.item(),\n",
    "            'volatility': volatility_loss.item(), 'spatial': spatial_loss.item(),\n",
    "            'total': total.item()\n",
    "        }\n",
    "        return total, components\n",
    "\n",
    "\n",
    "criterion = HybridLoss(alpha=0.4, beta=0.2, gamma=0.2, delta=0.2,\n",
    "                       adj_matrix=adj_matrix).to(DEVICE)\n",
    "print(f'HybridLoss: a={criterion.alpha}, b={criterion.beta}, '\n",
    "      f'g={criterion.gamma}, d={criterion.delta}')"
]))

